"""
summon_twin_agent.py — drop-in cartridge for the rapp-installer'd brainstem.

Drop this file into ~/.brainstem/agents/. Restart the brainstem. The
canonical agent loader picks it up; the LLM gets a new tool called
`SummonTwin`. Now in chat:

    User: "I want a memorial twin for my grandmother who passed last year."
    Model: <calls SummonTwin(twin_name="grandma-twin", kind="memorial",
                              description="my grandmother who passed in 2025")>
    Tool result: "Created memorial twin grandma-twin
                  (rappid 7bd3...). Located at ~/.rapp/twins/7bd3.../,
                  registered at port 7081. Boot it with:
                  SOUL_PATH=~/.rapp/twins/7bd3.../soul.md
                  ~/.brainstem/start.sh --port 7081"

This is the cartridge contract: the agent does ALL the work in
.perform() and returns a string the model relays to the user. No
out-of-band CLI, no separate process, no console-side change required.

The agent CONFORMS to the rapp-installer'd brainstem's interface (it's
a BasicAgent subclass with name + metadata + perform). It does not
modify the brainstem; it does not bundle its own kernel. The brainstem
is the static ancestor; this agent is a cartridge that plugs in.

Outputs land at canonical paths:
    ~/.rapp/twins/<rappid>/         — the new twin's workspace
    ~/.config/rapp/peers.json       — peer registry entry (rapp-peers/1.1)
"""

